void solve()
{
    int n;
    cin >> n;
    for (int i = 1; i <= 26; i++)
    {
        for (int j = 1; j <= 26; j++)
        {
            for (int k = 1; k <= 26; k++)
            {
                if (i + j + k == n)
                {
                    string s;
                    s.pb(i + 'a' - 1);
                    s.pb(j + 'a' - 1);
                    s.pb(k + 'a' - 1);
                    cout << s << endl;
                    return;
                }
            }
        }
    }
}
