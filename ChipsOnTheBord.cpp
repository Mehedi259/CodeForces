void solve()
{
    ll n, sum1 = 0, sum2 = 0;
    cin >> n;
    vl a(n), b(n);
    for (int i = 0; i < n; i++)
    {
        cin >> a[i];
    }
    for (int i = 0; i < n; i++)
    {
        cin >> b[i];
    }
    sort(all(a));
    sort(all(b));

    for (int i = 0; i < n; i++)
    {
        sum1 += a[0] + b[i];
        sum2 += a[i] + b[0];
    }
  
    if (sum1 <= sum2)
    {
        cout << sum1 << endl;
    }
    else
    {
        cout << sum2 << endl;
    }
}
